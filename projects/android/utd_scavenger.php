<h3>University of Texas at Dallas - UTD Scavenger</h3>
<p>Below is a collection of some of the mockups we used to create UTD
Scavenger. The application allowed students on the UTD campus to meet
each other during a scavenger hunt for orientation weekend. It used NFC as a
check-in feature to prove that the students had physically found the items
and a map to give students a clue of where the next item was located.</p>

