<h3>University of Texas at Dallas Senior Design Project</h3>
<p>Below was the final presentation for the Open Source Diagram Editor our team created
    for our sponsor. The Diagram Editor allowed users to drop mock network devices onto a
    canvas and connect them. The final product could then be exported to a manifest file
    which could be run in a separate program to create a cost estimation for the custom
    Network Topology. A Node Webkit application packaged all the files together to create
    a windows installer or an OSX installer depending on the situation.</p>

